#include "programdata.h"
#include "program_memory.h"


#define ADC_LUT_COMPRESSION_LEVEL 2
#define ADC_LUT_SIZE 1024 / ADC_LUT_COMPRESSION_LEVEL

/*ADC Look-up tables, each table is 1024/ADC_LUT_COMPRESSION_LEVEL bytes long*/
static const int8_t adc_LUT[][ADC_LUT_SIZE];

/*LUT is in the form of 8-bit signed values, so we need to add offset to get the correct value*/
/*This is the offset for the 10-bit ADC*/
static const int8_t ADC_LUT_OFFSET = 64;

int16_t PROGRAMDATA_get_ADC_lut_value(const PROGRAMDATA_ADC_LUT_t lut, uint16_t adc_value)
{
    int16_t result = PROGRAMDATA_BAD_VAL;
    adc_value >>= (ADC_LUT_COMPRESSION_LEVEL-1);
    /*Edge values are invalid*/
    if(ADC_LUT_SIZE-1 > adc_value && 0 < adc_value)
    {
        result = ADC_LUT_OFFSET + PROGRAM_MEMORY_read_byte(&adc_LUT[lut][adc_value]);
    }

    return result;
}

